    private void btnApplyjobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyjobActionPerformed
        int selectedRow = tblJob.getSelectedRow();

        if (selectedRow >= 0) {
            Application application = new Application();
            Job job = (Job) tblJob.getValueAt(selectedRow, 0);
            application.setJob(job);
            application.setApplicant(userAccount.getApplicant());
            
            //Job job = (Job) tblJob.getValueAt(selectedRow, 0);
            ApplicantWorkRequest request = new ApplicantWorkRequest();
            request.setApplicationID(application.getApplicationID());
            request.setJob(job);
            request.setStatus("Applied");
            request.setSender(userAccount);
            request.setSendername(userAccount.getApplicant().getFirstName());
            request.setEmail(userAccount.getEmail());
            organization.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
            business.getWorkQueue().getWorkRequestList().add(request);
            populateWorkQueueTable();
    }//GEN-LAST:event_btnApplyjobActionPerformed
    else
            JOptionPane.showMessageDialog(btnApplyjob, "Please select a job from Jobs Available table to apply");
    }    
    

