    public void populateJobTable() {
        DefaultTableModel model = (DefaultTableModel) tblJob.getModel();

        model.setRowCount(0);
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    for (Job job : org.getJobDirectory().getJoblist()) {
                        if (job.getCategory().equals("Priviledged")) {
                            Object[] row = new Object[3];
                            row[0] = job;
                            row[1] = job.getJobID();
                            //row[2] = job.getCategory();
                            model.addRow(row);
                        }
                    }
                }
            }
        }
    }

    public void populateWorkQueueTable() {
        DefaultTableModel model = (DefaultTableModel) requestTable.getModel();

        model.setRowCount(0);
        for (UserAccount useraccount : organization.getUserAccountDirectory().getUserAccountList()) {
            for (WorkRequest work : useraccount.getWorkQueue().getWorkRequestList()) {
                if (useraccount.getApplicant() != null) {
                    if (useraccount.getApplicant().getUserName().equals(userAccount.getApplicant().getUserName())) {
                        if (work instanceof ApplicantWorkRequest) {
                            Object[] row = new Object[4];
                            row[0] = work.getStatus();
                            row[1] = ((ApplicantWorkRequest) work).getApplicationID();
                            row[2] = ((ApplicantWorkRequest) work).getJob().getJobPosition();
                            row[3] = ((ApplicantWorkRequest) work).getSender();
                            model.addRow(row);
                        }
                    }
                }
            }
        }
    }

