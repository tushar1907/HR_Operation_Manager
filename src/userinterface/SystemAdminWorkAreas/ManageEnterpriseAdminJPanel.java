    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();

        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordTf.getPassword());
        String name = nameJTextField.getText();
        String email =nameJTextField1.getText();
        if(!username.equals("") && !password.equals("") && !name.equals("") && !email.equals(""))
        {
        Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);
        if (enterprise.getUserAccountDirectory().checkIfUsernameIsUnique(username)) {
            UserAccount account = null;
        if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.Company) {
                account = enterprise.getUserAccountDirectory().createEmployeeUserAccount(username, password, employee, new EnterpriseAdminRole(),email);
            } else if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.NGO) {
                account = enterprise.getUserAccountDirectory().createEmployeeUserAccount(username, password, employee, new EnterpriseAdminRole(),email);
            } else if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.OldAgeHome) {
                account = enterprise.getUserAccountDirectory().createEmployeeUserAccount(username, password, employee, new EnterpriseAdminRole(),email);
            }else if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.Veterans) {
                account = enterprise.getUserAccountDirectory().createEmployeeUserAccount(username, password, employee, new EnterpriseAdminRole(),email);
            }

            populateTable();
        }
        else {
            JOptionPane.showMessageDialog(null, "Please enter unique username", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        JOptionPane.showMessageDialog(submitJButton, "Submitted succesfully");
        }
        else
            JOptionPane.showMessageDialog(submitJButton, "Please enter valid details");
        
        usernameJTextField.setText("");
        passwordTf.setText("");
        nameJTextField.setText("");
        nameJTextField1.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed

